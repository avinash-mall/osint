"""
Dataset loader for the Inria aerial fallback corpus.

This module reads the pre-generated chips.json written by
``scripts/fetch_eval_datasets._fetch_inria_fallback()``.  If the JSON file
does not yet exist it is created automatically on first access.

Usage
-----
::

    from scripts.eval_datasets.inria_fallback import load

    chips = load(max_chips=20)
    for chip in chips:
        print(chip["chip_path"], chip["gt_boxes"])

Each returned dict has the shape::

    {
        "chip_path": str,          # absolute path to the PNG chip
        "modality": "rgb",
        "prompts": list[str],      # open-vocab prompts for SAM3
        "gt_boxes": [
            {
                "label": str,
                "bbox_xyxy": [x1, y1, x2, y2],  # ints
            },
            ...
        ],
    }

.. note::
   The ground-truth boxes are **synthetic placeholders** (full-image bounding
   boxes labelled "large-vehicle") generated by the fetcher.  They are
   intended only to exercise the evaluation pipeline end-to-end, not to
   measure actual detection accuracy.
"""
from __future__ import annotations

import json
import sys
from pathlib import Path

# Prompts suited to Inria aerial imagery and SAM3's open-vocab vocabulary
_PROMPTS: list[str] = ["vehicle", "large vehicle", "car", "truck", "building"]

_REPO_ROOT = Path(__file__).resolve().parents[2]
_CHIPS_JSON = _REPO_ROOT / "inference-sam3" / "eval" / "datasets" / "inria_fallback" / "chips.json"


def _ensure_chips_json(max_chips: int) -> None:
    """Run the fetcher if chips.json is absent or empty."""
    if _CHIPS_JSON.exists() and _CHIPS_JSON.stat().st_size > 0:
        return

    # Dynamically import to avoid a hard circular dependency at module level
    try:
        # Allow running from the repo root or from within scripts/
        sys.path.insert(0, str(_REPO_ROOT / "scripts"))
        import fetch_eval_datasets as _fetcher  # type: ignore[import]
    except ImportError as exc:
        raise ImportError(
            "Could not import fetch_eval_datasets.  "
            "Make sure scripts/ is on PYTHONPATH or run from the repo root."
        ) from exc

    _fetcher._fetch_inria_fallback(max_chips=max_chips)


def load(max_chips: int = 20) -> list[dict]:
    """
    Return a list of chip descriptors for the Inria fallback corpus.

    Parameters
    ----------
    max_chips:
        Maximum number of chips to return.  Passed to the fetcher if the
        chips.json file needs to be generated.

    Returns
    -------
    list[dict]
        Each dict has keys: ``chip_path``, ``modality``, ``prompts``,
        ``gt_boxes``.
    """
    _ensure_chips_json(max_chips=max_chips)

    if not _CHIPS_JSON.exists():
        return []

    chips: list[dict] = []
    with _CHIPS_JSON.open() as fh:
        for line in fh:
            line = line.strip()
            if not line:
                continue
            record = json.loads(line)
            chips.append(
                {
                    "chip_path": record["chip_path"],
                    "modality": record.get("modality", "rgb"),
                    "prompts": _PROMPTS,
                    "gt_boxes": [
                        {
                            "label": box["label"],
                            "bbox_xyxy": box["bbox_xyxy"],
                        }
                        for box in record.get("gt_boxes", [])
                    ],
                }
            )

    return chips[:max_chips]
